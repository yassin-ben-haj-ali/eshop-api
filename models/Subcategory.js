const mongoose = require('mongoose');
const { CATEGORY_TYPE } = require('../enums');

const subcategorySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    slug: {
      type: String,
      required: true,
      unique: true,
    },
    description: {
      type: String,
      required: true,
    },
    imageUrl: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      enum: Object.values(CATEGORY_TYPE),
      required: true,
    },
    products: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Product',
      },
    ],
  },
  {
    timestamps: true,
  },
);
const Subcategory = mongoose.model('Subcategory', subcategorySchema);

module.exports = Subcategory;
